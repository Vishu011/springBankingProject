package com.bank.aiorchestrator.integrations.account.dto;

import java.time.LocalDateTime;

/**
 * Mirror of AccountMicroservice's AccountResponse DTO.
 */
public class AccountResponse {
    private String accountId;
    private String userId;
    private String accountNumber;
    private String accountType; // Map enum to String to avoid tight coupling
    private Double balance;
    private String status;      // Map enum to String
    private LocalDateTime createdAt;

    public String getAccountId() {
        return accountId;
    }

    public void setAccountId(String accountId) {
        this.accountId = accountId;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getAccountNumber() {
        return accountNumber;
    }

    public void setAccountNumber(String accountNumber) {
        this.accountNumber = accountNumber;
    }

    public String getAccountType() {
        return accountType;
    }

    public void setAccountType(String accountType) {
        this.accountType = accountType;
    }

    public Double getBalance() {
        return balance;
    }

    public void setBalance(Double balance) {
        this.balance = balance;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public LocalDateTime getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(LocalDateTime createdAt) {
        this.createdAt = createdAt;
    }
}
