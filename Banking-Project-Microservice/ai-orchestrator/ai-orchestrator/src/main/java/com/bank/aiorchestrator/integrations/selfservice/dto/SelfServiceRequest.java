package com.bank.aiorchestrator.integrations.selfservice.dto;

import java.time.LocalDateTime;
import java.util.List;

/**
 * Mirror of SelfServiceRequest entity/response with looser typing for enums.
 */
public class SelfServiceRequest {
    private String requestId;
    private String userId;
    private String type;          // NAME_CHANGE, DOB_CHANGE, ADDRESS_CHANGE
    private String status;        // SUBMITTED, APPROVED, REJECTED
    private String payloadJson;   // JSON with requested changes
    private List<String> documents;
    private String adminComment;
    private String reviewerId;
    private LocalDateTime submittedAt;
    private LocalDateTime reviewedAt;

    public String getRequestId() {
        return requestId;
    }

    public void setRequestId(String requestId) {
        this.requestId = requestId;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getPayloadJson() {
        return payloadJson;
    }

    public void setPayloadJson(String payloadJson) {
        this.payloadJson = payloadJson;
    }

    public List<String> getDocuments() {
        return documents;
    }

    public void setDocuments(List<String> documents) {
        this.documents = documents;
    }

    public String getAdminComment() {
        return adminComment;
    }

    public void setAdminComment(String adminComment) {
        this.adminComment = adminComment;
    }

    public String getReviewerId() {
        return reviewerId;
    }

    public void setReviewerId(String reviewerId) {
        this.reviewerId = reviewerId;
    }

    public LocalDateTime getSubmittedAt() {
        return submittedAt;
    }

    public void setSubmittedAt(LocalDateTime submittedAt) {
        this.submittedAt = submittedAt;
    }

    public LocalDateTime getReviewedAt() {
        return reviewedAt;
    }

    public void setReviewedAt(LocalDateTime reviewedAt) {
        this.reviewedAt = reviewedAt;
    }
}
