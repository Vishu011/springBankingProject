package com.bank.aiorchestrator.integrations.loan.dto;

import java.math.BigDecimal;
import java.time.LocalDateTime;

/**
 * Mirror of loan-service LoanResponseDto with looser typing to avoid tight coupling.
 */
public class LoanResponseDto {
    private String loanId;
    private String userId;
    private String loanType;          // enum as String
    private BigDecimal amount;
    private Integer tenureInMonths;
    private Double interestRate;
    private String status;            // enum as String e.g. PENDING, APPROVED, REJECTED
    private LocalDateTime applicationDate;

    public String getLoanId() {
        return loanId;
    }

    public void setLoanId(String loanId) {
        this.loanId = loanId;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getLoanType() {
        return loanType;
    }

    public void setLoanType(String loanType) {
        this.loanType = loanType;
    }

    public BigDecimal getAmount() {
        return amount;
    }

    public void setAmount(BigDecimal amount) {
        this.amount = amount;
    }

    public Integer getTenureInMonths() {
        return tenureInMonths;
    }

    public void setTenureInMonths(Integer tenureInMonths) {
        this.tenureInMonths = tenureInMonths;
    }

    public Double getInterestRate() {
        return interestRate;
    }

    public void setInterestRate(Double interestRate) {
        this.interestRate = interestRate;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public LocalDateTime getApplicationDate() {
        return applicationDate;
    }

    public void setApplicationDate(LocalDateTime applicationDate) {
        this.applicationDate = applicationDate;
    }
}
